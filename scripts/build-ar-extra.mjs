const out = [];
const seen = new Set();
const push = (e) => { if (!seen.has(e.slug)) { seen.add(e.slug); out.push(e); } };

for (const [phrase, cat] of SYMBOLS) {
  const slug = slugify(phrase);
  push({ slug, phrase, parent: slug, type: "symbol", priority: 2, arCategory: cat });
}
for (const [parentPhrase, subs] of Object.entries(COMBOS)) {
  const parentSlug = slugify(parentPhrase);
  const parentCat = (SYMBOLS.find(([p]) => p === parentPhrase) || [])[1] || "الأشياء والرموز";
  for (const sub of subs) {
    const slug = slugify(sub);
    push({ slug, phrase: sub, parent: parentSlug, type: "combo", priority: 3, arCategory: parentCat });
  }
}

