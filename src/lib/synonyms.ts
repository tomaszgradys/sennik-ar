import { catalogEntry, isPublished } from "./catalog";
import { slugify, arNorm } from "./polish";

// طبقة المرادفات للبحث (بلا ذكاء اصطناعي): تربط الصيغ العامية/المرادفة التي لا
// يلتقطها التطابق الحرفي ولا تسوية «ال» بالرمز المنشور المقابل. مثال: «فلوس»،
// «مصاري» → «المال»؛ «عربية»، «موتر» → «سيارة»؛ «دهب» → «الذهب».
//
// القاعدة: المفتاح = الرمز المنشور (الهدف)، القيمة = صيغه المرادفة. نبني خريطة
// عكسية variant→target مع ضمانتين:
//   1) الهدف يجب أن يكون رمزًا منشورًا فعلًا (وإلا نتجاهل المجموعة).
//   2) لا نخطف صيغة لها صفحتها الخاصة: إن كان سلاگ الـ variant رمزًا منشورًا،
//      نتركه لصفحته (التطابق المباشر يفوز)، فلا نضيفه كمرادف.
// هكذا لا نولّد تفسيرًا جديدًا، بل نوجّه فقط إلى أفضل هاسل موجود.
const SYN_SOURCE: Record<string, string[]> = {
  الثعبان: ["حنش", "ثعابين", "افاعي"],
  المال: ["فلوس", "مصاري", "دراهم", "دنانير", "عملة", "كاش", "أموال", "مبلغ"],
  الموت: ["وفاة", "الوفاة", "منية", "الممات", "الموتى"],
  سيارة: ["عربية", "موتر", "مركبة", "أوتومبيل", "سيارات"],
  الذهب: ["دهب", "الدهب", "مجوهرات", "صياغة", "ذهبية"],
  فتاة: ["بنت", "بنية", "صبية", "فتيات", "بنات"],
  صبي: ["ولد", "غلام", "صبيان", "أولاد"],
  عصفور: ["طير", "طائر", "عصفورة", "عصافير", "طيور"],
  الجن: ["عفريت", "عفاريت", "جني", "الجني", "جان"],
  الأسنان: ["ضرس", "ضروس", "سنة", "أضراس", "الضرس"],
  البحر: ["محيط", "المحيط", "بحار", "أمواج"],
  حمل: ["حامل", "حبلى", "حبل", "حوامل"],
  سمك: ["سمكة", "سمكات", "أسماك"],
  مطر: ["غيث", "أمطار", "المطر"],
  ذئب: ["ديب", "ذئاب", "ذياب"],
  أسد: ["سبع", "ليث", "أسود", "غضنفر"],
  الزواج: ["زفاف", "الزفاف", "زواجات", "عرسان"],
  العروس: ["عروسة", "عرايس"],
  القبر: ["قبور", "مقبرة", "المقبرة", "ضريح", "لحد"],
  النار: ["لهب", "لهيب", "نيران"],
  الماء: ["مياه", "المياه", "ميه", "مويه"],
  بقرة: ["بقر", "أبقار"],
  قرد: ["قرود", "سعدان"],
  ثعلب: ["ثعالب"],
};

// خريطة variantالسلاگ → targetالسلاگ، مبنية مرة واحدة بعد التحقق.
const MAP = new Map<string, string>();
for (const [target, variants] of Object.entries(SYN_SOURCE)) {
  const tSlug = slugify(target);
  const te = catalogEntry(tSlug);
  if (!te || te.type !== "symbol" || !isPublished(tSlug)) continue; // الهدف غير منشور → تجاهل
  for (const v of variants) {
    const vSlug = slugify(v);
    if (!vSlug || vSlug === tSlug) continue;
    // للـ variant صفحته الخاصة؟ اتركه لها (تطابق مباشر أدق).
    const ve = catalogEntry(vSlug);
    if (ve && ve.type === "symbol" && isPublished(vSlug)) continue;
    if (!MAP.has(vSlug)) MAP.set(vSlug, tSlug);
  }
}

// الرمز الهدف لصيغة مرادفة (على السلاگ الخام أو بعد تسوية «ال»)، أو null.
export function synonymTarget(slug: string): string | null {
  return MAP.get(slug) ?? MAP.get(arNorm(slug)) ?? null;
}
